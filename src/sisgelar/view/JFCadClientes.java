    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        tabela.getSelectionModel().addListSelectionListener(tabela);
        if (tabela.getSelectedRow() >= 0) {
            Pessoa p = new Pessoa();

            //            txtIdPessoa.setText(tabela.getValueAt(tabela.getSelectedRow(), 0).toString());
            //            txtPessoa.setText(""+tabela.getValueAt(tabela.getSelectedRow(), 1));
            String cpfCnpj = tabela.getValueAt(tabela.getSelectedRow(), 2).toString();
            if(cpfCnpj.length()>14){
                jcbPessoa.setSelectedItem("PJ");
            }else
            jcbPessoa.setSelectedItem("PF");
            //                txtCPF.setText(tabela.getValueAt(tabela.getSelectedRow(), 2).toString());
            //                txtEndereco.setText(tabela.getValueAt(tabela.getSelectedRow(), 3).toString());
            int pC = Integer.parseInt(tabela.getValueAt(tabela.getSelectedRow(), 0).toString());
            preencheCampos(pC);

            btnExcluir.setEnabled(true);
            btnAlterar.setEnabled(true);
            btnCadastrar.setEnabled(false);
        }
    }//GEN-LAST:event_tabelaMouseClicked

