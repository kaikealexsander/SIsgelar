        public void inicia(){
        jcbPessoa.addActionListener(new ActionListener() {

        @Override
        public void actionPerformed(ActionEvent event) {
            JComboBox<String> combo = (JComboBox<String>) event.getSource();
            String selectedBook = (String) combo.getSelectedItem();

            if (selectedBook.equals("PF")) {
                try {
                    System.out.println("Pessoa Fisica!");
                    txtCPF.setFormatterFactory(null);
                    javax.swing.text.MaskFormatter format = new javax.swing.text.MaskFormatter("###.###.###-##");
                    format.setPlaceholderCharacter('_');
                    txtCPF.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(format));
                    txtCPF.setValue(null);
                    lblNomeFantasia.setEnabled(false);
                    txtNomeFantasia.setEnabled(false);
                } catch (ParseException ex) {
                    Logger.getLogger(JFCadClientes.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else if (selectedBook.equals("PJ")) {
                try {
                    txtCPF.setFormatterFactory(null);
                    javax.swing.text.MaskFormatter format = new javax.swing.text.MaskFormatter("##.###.###/####-##");
                    format.setPlaceholderCharacter('_');
                    txtCPF.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(format));
                    txtCPF.setValue(null);
                    lblNomeFantasia.setEnabled(true);
                    txtNomeFantasia.setEnabled(true);
                } catch (ParseException ex) {
                    Logger.getLogger(JFCadClientes.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    });
    }

